    return nil unless web_icon_class.is_a?(String) && web_icon_class.present?

    # Normalize input: remove "fa-solid", "fa-brands", etc.
    # Examples:
    #   "fa-solid fa-code"     â†’ "code"
    #   "fa-brands fa-github"  â†’ "github"
    #   "lightbulb-o"          â†’ "lightbulb-o"
    #   "fa-code"              â†’ "code"

    # Split by space and take last part
    parts = web_icon_class.split
    normalized = parts.last.downcase

    # If it starts with "fa-", strip it
    if normalized.start_with?("fa-")
      normalized = normalized[3..-1]
    end

    # Look up in map
    LATEX_ICON_MAP[normalized]
  end

  # Optional: Reverse lookup (for admin UI / debugging)
  def self.web_icon_name(latex_command)
    LATEX_ICON_MAP.key(latex_command)&.presence
  end
